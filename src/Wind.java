public class Wind {
    private int forceX;
    private int forceZ;

    public Wind(int forceX, int forceZ) {
        this.forceX = forceX;
        this.forceZ = forceZ;
    }

    public int getForceX() {
        return forceX;
    }

    public void setForceX(int forceX) {
        this.forceX = forceX;
    }

    public int getForceZ() {
        return forceZ;
    }

    public void setForceZ(int forceZ) {
        this.forceZ = forceZ;
    }
}
